#include  <iostream>
using namespace std;

int main() {
    int x = 5;  //deklaration af variabel, int da det er et heltal
    int y = 6; //deklaration af variabel, int da det er et heltal
    int sum = x + y; //deklaration af variabel, initialisering af sum som summen af x og y

    cout << x << '\n'; //udskrivning af værdien af x til konsollen
    cout << y << '\n'; //udskrivning af værdien af y til konsollen
    cout << sum << '\n'; //udskrivning af værdien af sum til konsollen

    return 0;
}
